{
  serializedData[0] = message->priority;
  serializedData[1] = message->sender;
  serializedData[2] = message->recipient;
  serializedData[3] = message->cmd;

  for (int i = 0; i < 7; i++)
  {
    serializedData[4 + i] = message->body[i];
  }
}

